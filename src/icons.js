export default (prefix, names) => {
    const icon = icons[prefix]
    let items = {}
    for (let name of names) {
        items[name] = icon.icons[name]
    }
    return {
        prefix: icon.prefix,
        lastModified: icon.lastModified,
        aliases: {},
        width: icon.width,
        height: icon.height,
        icons: items,
    }
}